&lt;a href="#" class="btn <strong>btn-primary</strong> <em>btn-large btn-small btn-mini</em>"&gt;Primary&lt;/a&gt;

<strong>Note:</strong> 	All buttons <strong>MUST</strong> have the <strong>.btn</strong> class, followed by optional styling classes;
	Replace <em>[btn-*]</em> bellow to any of the button classes eg. <em>btn-default</em>, <em>btn-mini</em>, etc.
	Also, all the <em>btn-*</em> classes can be used on most HTML elements, like <strong>&lt;span&gt;</strong>, <strong>&lt;button&gt;</strong>, 
	<strong>&lt;input&gt;</strong>, <strong>&lt;a&gt;</strong>, etc.

&lt;!-- Disabled Button --&gt;
&lt;button class="btn <em>[btn-*]</em>" disabled="disabled"&gt;Disabled&lt;/button&gt;
